# Data Types

# String/char
string = "Hello"
char = string[(len(string)-1)]
print(char)

# Integers
integer = 123;
anotherInteger = 321;

print(f"a+b= {(integer+anotherInteger)}")

# float
float = 123.13
anotherFloat = 321.31
print(f"a+b= {float+anotherFloat}")

# Booleans
bool = float == anotherFloat
print(f"Is a = b? {bool}")